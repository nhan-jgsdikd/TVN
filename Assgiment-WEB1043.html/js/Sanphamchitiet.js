function renderProducts() {
    const container = document.querySelector('.product-list');
    container.innerHTML = '';

    products.forEach(product => {
        const productHTML = `
            <div class="product" data-id="${product.id}">
                <img src="${product.image}" alt="${product.title}">
                <div class="product-info">
                    <h3>${product.title}</h3>
                </div>
            </div>
        `;
        container.insertAdjacentHTML('beforeend', productHTML);
    });
}




var dongmodal = document.getElementById('product-modal')
dongmodal.style.display = 'none'
function openModal(product) {
    document.getElementById('product-image').src = product.image;
    document.getElementById('product-price').textContent = `Giá Bán : ${product.price}`;
    document.getElementById('product-title').textContent = product.title;
    document.getElementById('product-category').textContent = product.category;
    document.getElementById('product-author').textContent = product.Author;
    document.getElementById('product-episodes').textContent = product.Episodes;
    document.getElementById('product-time').textContent = product.Time;
    document.getElementById('product-anotherName').textContent = product.Another_name;
    document.getElementById('product-description').textContent = product.description;
    document.getElementById('product-modal').style.display = 'block';
}


function closeModal() {
    document.getElementById('product-modal').style.display = 'none';
}








document.addEventListener('DOMContentLoaded', () => {
    renderProducts();


    document.querySelectorAll('.product').forEach(item => {
        item.addEventListener('click', function() {
            const productId = this.getAttribute('data-id');
            const product = products.find(p => p.id == productId);
            if (product) {
                openModal(product);
            } else {
                console.error(`Product with ID ${productId} not found.`);
            }
        });
    });


    document.querySelector('#product-modal .close').addEventListener('click', closeModal);


    const addToCartButton = document.getElementById('add-to-cart');
    if (addToCartButton) {
        addToCartButton.addEventListener('click', function() {
            const image = document.getElementById('product-image').src;
            const title = document.getElementById('product-title').textContent;
            const category = document.getElementById('product-category').textContent;
            const author = document.getElementById('product-author').textContent;
            const episodes = document.getElementById('product-episodes').textContent;
            const time = document.getElementById('product-time').textContent;
            const anotherName = document.getElementById('product-anotherName').textContent;
            const description = document.getElementById('product-description').textContent;

            const priceText = document.getElementById('product-price') ? document.getElementById('product-price').textContent.replace("Giá Bán : ", "") : "0";
            const price = parseFloat(priceText.replace(/[^0-9.-]+/g, ""));

            const product = {
                "hinh": image,
                "ten": title,
                "category": category,
                "author": author,
                "episodes": episodes,
                "time": time,
                "anotherName": anotherName,
                "description": description,
                "gia": price,
                "soluong": 1
            };


            const cart = JSON.parse(localStorage.getItem('cart')) || [];
            cart.push(product);
            localStorage.setItem('cart', JSON.stringify(cart));

            const slsp = document.getElementById('slsp');
            if (slsp) {
                slsp.innerHTML = `(${cart.length})`;
            }

            alert('Sản phẩm đã được thêm vào giỏ hàng!');
        });
    }


    loadCartData();
});
